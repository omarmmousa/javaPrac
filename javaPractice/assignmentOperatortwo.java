class AssignmentOperatorTwo{
  public static void main(String args[]){
    int a = 10;
    a+= 3; // a + 3 = 10 + 3 --> 13
    System.out.println(a);
    a-= 4; // a - 4 = 13 - 4 --> 9
    System.out.println(a);
    a*=2; // a * 2 = 9 * 2 --> 18
    System.out.println(a);
    a/=2;// a / 2 = 18/2 --> 9
    System.out.println(a);

  }
}
